#include "libft.h"

int	ft_isalnum(int c)
{
	int	is_alnum;

	is_alnum = ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'));
	return (is_alnum || (c >= '0' && c <= '9'));
}
